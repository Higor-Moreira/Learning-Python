"""
Como trabalhar com porcentagem usando Python

1º conceito - 100% é sempre igual a 1, logo as porcentagens 

30%, 50%, 80% correspodem a 0.3 0.5 e 0.8

2º conceito - para saber qual o valor, basta multiplicar a porcentagem
pelo valor. 

"""

preco = 1000 # valor de um produto

desconto = 0.25 # desconto da loja

valor_desconto = preco * desconto # para saber qual o valor do desconto

print(preco, valor_desconto)


